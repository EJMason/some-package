/**
 * Adds 2 numbers together
 * @param n1 first number to add
 * @param n2 second number to add
 */
export function addTwo(n1: number, n2: number): number {
    return n1 + n2
}

/**
 *
 * @param n1 first number
 * @param n2 second number
 * @param n3 third number
 */
export function addThree(n1: number, n2: number, n3: number): number {
    return n1 + n2 + n3
}

/**
 * This will add 5 to ANYTHING!! ANY NUMBER!
 * @param n1 first number
 */
export function addFiveToAnyNumber(n1: number): number {
    return n1 + 5
}
